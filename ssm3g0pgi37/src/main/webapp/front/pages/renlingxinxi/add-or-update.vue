				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.dengjishijian =  this.$utils.getCurDateTime();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.renlingzhanghao = this.user.zhanghao
			this.ro.renlingzhanghao = true;
			this.ruleForm.renlingren = this.user.xingming
			this.ro.renlingren = true;
			this.ruleForm.lianxidianhua = this.user.shouji
			this.ro.lianxidianhua = true;



